
i = 1 
while i <= 5 :
    j = i
    while j < 5 :
        print(f" ",end="")
        j = j + 1
    k = 1
    while k <= i :
        print(f"* ",end="")
        k = k + 1
    print("")
    i = i + 1

l = 4
while l >= 1 :
    m = l
    while m < 5 :
        print(f" ",end="")
        m = m + 1
    n = 1
    while n <= l :
        print(f"* ",end="")
        n = n + 1
    print("")
    l = l - 1